        static string generateBirthDate()
        {
            Random rnd = new Random();
            DateTime startDate = new DateTime(1960, 1, 1);
            DateTime endDate = DateTime.Now.AddYears(-24); // 24 yaş sınırı için en geç tarih

            // Rastgele bir tarih aralığı oluşturma
            TimeSpan range = endDate - startDate;
            TimeSpan randomSpan = new TimeSpan((long)(rnd.NextDouble() * range.Ticks));
            DateTime randomDate = startDate + randomSpan;

            // Gün, ay, yıl olarak formatlama
            string formattedDate = $"{randomDate.Day:D2}.{randomDate.Month:D2}.{randomDate.Year}";
            return formattedDate;
        }
