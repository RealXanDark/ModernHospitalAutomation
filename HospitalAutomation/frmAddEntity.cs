            int digit5 = rnd.Next(0, 10);
            int digit6 = rnd.Next(0, 10);
            int digit7 = rnd.Next(0, 10);
            int digit8 = rnd.Next(0, 10);
            int digit9 = rnd.Next(0, 10);
            int digit10 = Math.Abs((((digit1 + digit3 + digit5 + digit7 + digit9) * 7) - (digit2 + digit4 + digit6 + digit8)) % 10);
